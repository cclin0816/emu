use super::*;

fn f32_op2_check(vals: &[u32], exps: &[(u32, u8)], op: FpBinaryOp) {
    let mut fpu = Fpu::default();
    let mut idx = 0usize;
    let mut bad_pass = false;
    for &v1 in vals {
        for &v2 in vals {
            let exp = exps[idx];
            fpu.u32_mv_f32(0, v1);
            fpu.u32_mv_f32(1, v2);
            fpu.clr_all_fpe();
            fpu.binary_op(0, 0, 1, Precision::S, op);
            let res = (fpu.f32_mv_u32(0), fpu.get_fpe().as_u8());
            if exp != res {
                print!(
                    "op: {:?},\nv1: {:#x},\nv2: {:#x},\nexpected: {:#x?}\nresult: {:#x?}\n",
                    op, v1, v2, exp, res,
                );
                bad_pass = true;
            }
            idx += 1;
        }
    }
    if bad_pass {
        panic!("f32_op2_check failed")
    }
}

